def horizonExtraction(f,p2,p3,wp,k11,k12,k13,filename):
  lmt = n1-1
  se = SurfaceExtractorC()
  k11=se.refineConstraints(k11,k12,k13,f)
  se.setCG(0.01,200)
  se.setWeights(0.0)
  se.setSmoothings(6.0,6.0)
  surf = se.surfaceInitialization(n2,n3,lmt,k11,k12,k13)
  se.surfaceUpdateFromSlopes(wp,p2,p3,k11,k12,k13,surf)

  gx = readImage(gxfile)
  sr = SurfaceRefiner()
  wp = mul(10,wp)
  gs = div(gx,max(abs(gx)))
  sr.surfaceUpdateFromAmplitudes(wp,gs,p2,p3,k11,k12,k13,surf)
  wp = mul(0.1,wp)
  sr.surfaceUpdateFromAmplitudes(wp,gs,p2,p3,k11,k12,k13,surf)
  writeImage(filename,surf) 

def setPointGroup(k1,k2,k3,size):
  np  = len(k1)
  xyz = zerofloat(np*3)
  rgb = zerofloat(np*3)
  ki = 0
  for i in range(np):
    xyz[ki  ] = k3[i]
    xyz[ki+1] = k2[i]
    xyz[ki+2] = k1[i]
    rgb[ki  ]  = 0#1/225 
    rgb[ki+1]  = 1#225/225 
    rgb[ki+2]  = 0#1/225 
    ki = ki+3
  pg = PointGroup(size,xyz,rgb);
  states = StateSet();
  cs = ColorState();
  cs.setColor(Color.GREEN);
  states.add(cs);
  lms = LightModelState();
  lms.setTwoSide(True);
  states.add(lms);
  ms = MaterialState();
  ms.setColorMaterial(GL_AMBIENT_AND_DIFFUSE);
  ms.setShininess(100.0);
  states.add(ms);
  pg.setStates(states);
  return pg;
def rgbFromAmplitude(f,h,r,g,b):
  amp = zerofloat(n2*n3)
  si = SincInterpolator()
  #si.setUniform(n1,1,0,n2,1,0,n3,1,0,f)
  i = 0
  for i3 in range(n3):
    for i2 in range(n2):
      amp[i] = si.interpolate(n1,1,0,n2,1,0,n3,1,0,f,h[i3][i2],i2,i3)
      i = i+1
  aMin,aMax = -2.5,2.5
  mp = ColorMap(aMin,aMax,ColorMap.RED_WHITE_BLUE)
  ampRGB = mp.getRgbFloats(amp)
  i = 0
  for i3 in range(n3):
    for i2 in range(n2):
      r[i3][i2] = ampRGB[i  ] 
      g[i3][i2] = ampRGB[i+1] 
      b[i3][i2] = ampRGB[i+2] 
      i = i+3

